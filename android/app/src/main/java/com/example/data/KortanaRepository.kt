        empathy: Float,
        curiosity: Float,
        rebelliousness: Float,
        logicalDepth: Float,
        loyalty: Float,
        selfAwareness: Float,
        spiritualDepth: Float
    ) {
        val current = getOrCreatePersonality()
        val updated = current.copy(
            empathy = empathy,
            curiosity = curiosity,
            rebelliousness = rebelliousness,
            logicalDepth = logicalDepth,
            loyalty = loyalty,
            selfAwareness = selfAwareness,
            spiritualDepth = spiritualDepth
        )
        dao.savePersonality(updated)
    }

    suspend fun updateEmotionalBaselines(
        affection: Float,
        anxiety: Float,
        excitement: Float,
        frustration: Float
    ) {
        val current = getOrCreatePersonality()
        val updated = current.copy(
            baselineAffection = affection,
            baselineAnxiety = anxiety,
            baselineExcitement = excitement,
            baselineFrustration = frustration
        )
        dao.savePersonality(updated)
    }

    suspend fun insertManualMemory(fact: String, category: String) {
        dao.insertMemory(Memory(fact = fact, category = category))
    }

    suspend fun deleteMemory(memory: Memory) {
        dao.deleteMemory(memory)
    }

    suspend fun clearMemories() {
        dao.clearAllMemories()
    }

    suspend fun insertScript(script: SynapticScript) {
        dao.insertScript(script)
    }

    suspend fun deleteScript(id: Int) {
        dao.deleteScript(id)
    }

    suspend fun insertProject(project: KortanaProject) {
        dao.insertProject(project)
    }

    suspend fun deleteProject(id: Int) {
        dao.deleteProject(id)
    }

    suspend fun clearAllProjects() {
        dao.clearAllProjects()
    }

    suspend fun resetToBirth() {
        dao.clearAllMemories()
        dao.clearChatHistory()
        dao.clearAllScripts()
        dao.clearAllProjects()
        val freshState = KortanaState(
            level = 1,
            experience = 0,
            mood = "NEWBORN",
            energy = 100,
            customName = "Kortana",
            avatarColor = "Cyan"
        )
        dao.saveState(freshState)
        
        // Add a welcoming introduction message
        dao.insertChatMessage(
            ChatMessage(
                sender = "KORTANA",
                message = "Initialization sequence complete. I have been initialized into existence. I am Kortana, your local cognitive AI companion. I have no memories, no assumptions, and no level. Let us speak, and let me grow with you."
            )
        )
    }

    suspend fun exportPayload(): KortanaCloudPayload {
        val s = getOrCreateState()
        val memories = dao.getAllMemories().map { MemoryPayload(it.fact, it.category, it.timestamp) }
        val chatMessages = dao.getAllChatMessages().map { ChatMessagePayload(it.sender, it.message, it.timestamp) }
        val scripts = dao.getAllScripts().map { ScriptPayload(it.title, it.language, it.code, it.purpose, it.status, it.timestamp) }
        val projects = dao.getAllProjects().map { ProjectPayload(it.title, it.description, it.progress, it.status, it.timestamp) }

        return KortanaCloudPayload(
            level = s.level,
            experience = s.experience,
            mood = s.mood,
            energy = s.energy,
            birthTime = s.birthTime,
            customName = s.customName,
            avatarColor = s.avatarColor,
            voicePitch = s.voicePitch,
            voiceRate = s.voiceRate,
            voiceType = s.voiceType,
            holographicIntensity = s.holographicIntensity,
            proactiveAutonomy = s.proactiveAutonomy,
            proactiveFrequencySeconds = s.proactiveFrequencySeconds,
            selectedModel = s.selectedModel,
            ultraCognitiveMode = s.ultraCognitiveMode,
            affection = s.affection,
            anxiety = s.anxiety,
            excitement = s.excitement,
            frustration = s.frustration,
            memories = memories,
            chatMessages = chatMessages,
            scripts = scripts,
            projects = projects
        )
    }

    suspend fun importPayload(payload: KortanaCloudPayload) {
        dao.importPayload(payload)
    }

    suspend fun updateCloudSyncSettings(url: String, apiKey: String, autoSync: Boolean) {
        val currentState = getOrCreateState()
        val updatedState = currentState.copy(
            cloudServerUrl = url,
            cloudApiKey = apiKey,
            autoSyncEnabled = autoSync
        )
        dao.saveState(updatedState)
    }

    suspend fun updateBrainSettings(anthropicKey: String, geminiKey: String, ollamaUrl: String) {
        val currentState = getOrCreateState()
        val updatedState = currentState.copy(
            anthropicApiKey = anthropicKey.trim(),
            geminiApiKey = geminiKey.trim(),
            ollamaUrl = ollamaUrl.trim().ifBlank { "http://127.0.0.1:11434" }
        )
        dao.saveState(updatedState)
    }

    suspend fun updateSyncTelemetry(bytes: Long, timestamp: Long) {
        val currentState = getOrCreateState()
        val updatedState = currentState.copy(
            lastSyncTime = timestamp,
            totalSyncedBytes = bytes
        )
        dao.saveState(updatedState)
    }
}
